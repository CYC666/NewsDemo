#import "CollectUserPage.h"

@implementation CollectUserPage

///初始化
- (instancetype)initWithuserid:(NSString *)userid nickname:(NSString *)nickname realname:(NSString *)realname headicon:(NSString *)headicon mobile:(NSString *)mobile provincename:(NSString *)provincename cityname:(NSString *)cityname countyname:(NSString *)countyname address:(NSString *)address createdate:(NSString *)createdate {

    if (self = [super init]) {
        
        _userid = [NSString stringWithFormat:@"%@",userid];
        _nickname = [NSString stringWithFormat:@"%@",nickname];
        _realname = [NSString stringWithFormat:@"%@",realname];
        _headicon = [NSString stringWithFormat:@"%@",headicon];
        _mobile = [NSString stringWithFormat:@"%@",mobile];
        _provincename = [NSString stringWithFormat:@"%@",provincename];
        _cityname = [NSString stringWithFormat:@"%@",cityname];
        _countyname = [NSString stringWithFormat:@"%@",countyname];
        _address = [NSString stringWithFormat:@"%@",address];
        _createdate = [NSString stringWithFormat:@"%@",createdate];
        
    }
    
    return self;

}


@end
