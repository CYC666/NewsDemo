#import "LoopPlayImage.h"

@implementation LoopPlayImage

- (instancetype)initWithImageName:(NSString *)ImageName ImageFilePath:(NSString *)ImageFilePath IsExternalLink:(NSString *)IsExternalLink ObjectValue:(NSString *)ObjectValue {
    
    if (self = [super init]) {
        _ImageName = [NSString stringWithFormat:@"%@",ImageName];
        _ImageFilePath = [NSString stringWithFormat:@"%@",ImageFilePath];
        _IsExternalLink = [NSString stringWithFormat:@"%@",IsExternalLink];
        _ObjectValue = [NSString stringWithFormat:@"%@",ObjectValue];
    }
    
    return self;
}

@end
