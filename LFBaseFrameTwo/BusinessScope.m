#import "BusinessScope.h"

@implementation BusinessScope

///初始化
- (instancetype)initWithItemDetailId:(NSString *)ItemDetailId ItemName:(NSString *)ItemName ItemValue:(NSString *)ItemValue Description:(NSString *)Description {

    if (self = [super init]) {
        
        _ItemDetailId = [NSString stringWithFormat:@"%@",ItemDetailId];
        _ItemName = [NSString stringWithFormat:@"%@",ItemName];
        _ItemValue = [NSString stringWithFormat:@"%@",ItemValue];
        _Description = [NSString stringWithFormat:@"%@",Description];
        
    }
    
    return self;
}


@end
